package com.example.soohoon.community;




public class BoardData {

    public String boardImage;
    public String boardNic;
    public String boardNo;
    public String boardTitle;
    public String boardContent;
    public String boardDate;
    public String boardHit;

    public String getBoardImage() {
        return boardImage;
    }

    public void setBoardImage(String boardImage) {
        this.boardImage = boardImage;
    }

    public String getBoardNic() {
        return boardNic;
    }

    public void setBoardNic(String boardNic) {
        this.boardNic = boardNic;
    }

    public String getBoardNo() {
        return boardNo;
    }

    public void setBoardNo(String boardNo) {
        this.boardNo = boardNo;
    }

    public String getBoardTitle() {
        return boardTitle;
    }

    public void setBoardTitle(String boardTitle) {
        this.boardTitle = boardTitle;
    }

    public String getBoardContent() {
        return boardContent;
    }

    public void setBoardContent(String boardContent) {
        this.boardContent = boardContent;
    }

    public String getBoardDate() {
        return boardDate;
    }

    public void setBoardDate(String boardDate) {
        this.boardDate = boardDate;
    }

    public String getBoardHit() {
        return boardHit;
    }

    public void setBoardHit(String boardHit) {
        this.boardHit = boardHit;
    }
}